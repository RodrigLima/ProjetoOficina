     public void readJTable(){
          DefaultTableModel modelo = (DefaultTableModel) jFuncionario.getModel();
          modelo.setNumRows(0);
          FuncionarioDAO funcionarioDAO = new FuncionarioDAO();
          
          for(Funcionario f: funcionarioDAO.read()){
              modelo.addRow(new Object[]{
                  f.getId(),
                  f.getNome(),
                  f.getCargo(),
                  f.getCpf(),
                  f.getSenha()
                 
              });
            
              
          }
          
     }
     
